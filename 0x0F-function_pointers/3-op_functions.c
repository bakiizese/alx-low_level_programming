#include "3-calc.h"
/**
 * op_add - add 2 num
 * @a: num1
 * @b: num2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub 2 num
 * @a: num1
 * @b: num2
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul 2 num
 * @a: num1
 * @b: num2
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div 2 num
 * @a: num1
 * @b: num2
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder
 * @a: num1
 * @b: num2
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
